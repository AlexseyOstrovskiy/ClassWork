package com;

public class Rider {
    private String name;
    private  int age;
    private Object horse;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public Object getHorse() {
        return horse;
    }

    public void setHorse(Object horse) {
        this.horse = horse;
    }
}
